    function findTypes(element) {
        $('option:selected', element).removeAttr('selected');
        $('select[name=year]').attr('disabled', 'disabled')
        var element = $(element),
            type = $('select[name=type]'),
            selected = element.find('option:selected');
        $("input[name=brand-text]").val(selected.text());
        $("input[name=brand]").val(selected.data('brand'));
        $.get(location.origin + '/car/type/' + element.val())
            .done(function(response) {
                if (response.data) {
                    type.removeAttr('disabled')
                    var option = $(type.find('option').clone()[0]);
                    type.empty().append('<option value="">Model</option>')
                    response.data.forEach(function(item) {
                        type.append(option.clone().text(item.name).val(item.slug))
                    })
                }
            });
    }

    function findYears(element) {
        var element = $(element),
            target = $('select[name=year]');
        $("input[name=id_detail]").val(element.find('option:selected').data('id_detail'));
        $.get(location.origin + '/car/year/' + element.val())
            .done(function(response) {
                target.removeAttr('disabled')
                var option = $(target.find('option').clone()[0]);
                target.empty().append('<option value="">Tahun</option>')
                response.data.forEach(function(item) {
                    target.append(option.clone().text(item.year).val(item.year).data('id', item.id))
                })
            });
    }

    function onYearChange(element) {
